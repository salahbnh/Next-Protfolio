// One-time build artifact generator. Packs every skill logo into a single
// padded WebP sprite sheet and emits a UV map module, so the 3D scene fetches
// ONE texture instead of ~15 PNGs. Re-run with `node scripts/generate-skills-atlas.mjs`
// whenever the icon set changes.
//
//   - Output texture: public/skills-atlas.webp
//   - Output UV map:  app/components/skills/atlasMap.js
//
// The icon order here MUST stay in sync with skillsData.js (the map is keyed by
// the public icon path, so ordering only affects packing, not lookup).

import sharp from 'sharp';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { writeFile } from 'node:fs/promises';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PUBLIC = join(__dirname, '..', 'public');
const OUT_TEXTURE = join(PUBLIC, 'skills-atlas.webp');
const OUT_MAP = join(__dirname, '..', 'app', 'components', 'skills', 'atlasMap.js');

// Source logos in packing order (kept aligned with skillsData.js).
const ICONS = [
  '/reactsjs.png', '/nextjs.png', '/typescript.png', '/js.png',
  '/nodejs.png', '/java.png', '/springboot.png', '/firebase.png',
  '/mongodb.png', '/sql.png', '/git.png', '/unity.png',
  '/c.png', '/c-sharp.png', '/c++.png',
];

const CELL = 256;          // px per cell (crisp when an icon scales up on hover)
const PAD = 26;            // transparent breathing room inside each cell
const COLS = 4;
const ROWS = Math.ceil(ICONS.length / COLS);
const ATLAS_W = CELL * COLS;
const ATLAS_H = CELL * ROWS;

async function run() {
  const composites = [];
  const map = {};

  for (let i = 0; i < ICONS.length; i++) {
    const icon = ICONS[i];
    const col = i % COLS;
    const row = Math.floor(i / COLS);

    // Resize each logo to fit inside the padded cell, preserving aspect ratio
    // and alpha, centered on a transparent canvas.
    const inner = CELL - PAD * 2;
    const cell = await sharp(join(PUBLIC, icon))
      .resize(inner, inner, { fit: 'contain', background: { r: 0, g: 0, b: 0, alpha: 0 } })
      .extend({
        top: PAD, bottom: PAD, left: PAD, right: PAD,
        background: { r: 0, g: 0, b: 0, alpha: 0 },
      })
      .toBuffer();

    composites.push({ input: cell, left: col * CELL, top: row * CELL });

    // UV math: three.js texture origin is bottom-left, image rows go top-down.
    const repeatX = CELL / ATLAS_W;
    const repeatY = CELL / ATLAS_H;
    map[icon] = {
      offset: [col * repeatX, 1 - (row + 1) * repeatY],
      repeat: [repeatX, repeatY],
    };
  }

  await sharp({
    create: {
      width: ATLAS_W, height: ATLAS_H, channels: 4,
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    },
  })
    .composite(composites)
    .webp({ quality: 90, alphaQuality: 100 })
    .toFile(OUT_TEXTURE);

  const fileContents =
    `// AUTO-GENERATED by scripts/generate-skills-atlas.mjs — do not edit by hand.\n` +
    `// Maps each icon path to its UV offset/repeat within /skills-atlas.webp.\n` +
    `export const ATLAS_TEXTURE = '/skills-atlas.webp';\n` +
    `export const ATLAS_MAP = ${JSON.stringify(map, null, 2)};\n`;
  await writeFile(OUT_MAP, fileContents, 'utf8');

  const { size } = await sharp(OUT_TEXTURE).metadata().then(() => import('node:fs')).then((fs) => fs.statSync(OUT_TEXTURE));
  console.log(`atlas: ${ATLAS_W}x${ATLAS_H}, ${ICONS.length} icons -> ${OUT_TEXTURE} (${(size / 1024).toFixed(1)} KB)`);
  console.log(`map:   ${OUT_MAP}`);
}

run().catch((e) => { console.error(e); process.exit(1); });
